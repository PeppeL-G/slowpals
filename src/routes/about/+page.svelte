<h1>About</h1>
<p>
	Slowpals is a website developed by Peter Larsson-Green used for meeting
	and communicating with new friends all around the world in a stress
	free way. 
</p>

<h2>The problem Slowpals try to solve</h2>
<p>
	There are many websites and apps out there you can use to get to know
	new people around the world through written messages, but almost all
	of them have no restrictions on the messaging system, which often leads
	to low quality mass messages. Many who wants to message with others around
	the world find such messaging no fun, stressful, and they don't lead to
	good and meaningful relations.
</p>

<h2>How Slowpals solves the problem</h2>
<p>
	To get Slowpalers to write high quality messages, Slowpals introduces
	some restrictions on its messaging system:
</p>
<ul>
	<li>
		To prevent Slowpalers from sending mass messages, each Slowpaler gets
		her own birds, and each bird can only deliver one message at a time,
		and delivering a message takes some time (so if all your birds are busy
		delivering messages, you can't send any more until one has came back
		home to you)
	</li>
	<li>
		To make Slowpalers more aware of the location of the one they send the
		message to, the birds are realistic: the longer away the receiver is,
		the longer time it will take for your bird to deliver the message
	</li>
	<li>
		To enable Slowpalers to express gratitude and show they care about each
		other, the birds act as pets that needs to be taken care of. For
		example, when a bird has delivered a message, the receiver can feed the
		the bird to help it regain its strength and fly back to its owner quicker
	</li>
</ul>
<p>
	All this together will hopefully make people care and appreciate each
	other a bit more ❤️
</p>