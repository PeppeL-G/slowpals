<h1>Welcome!</h1>
<p>
	Welcome to Slowpals! On this platform you will find <em>slowpalers</em>,
	who are people that love exchanging long and meaningful messages with
	each other. To encourage people to send long and meaningful messages,
	you will get your own virtual bird when you become a member, and it is
	your bird that will deliver the messages you write to other users. And
	your bird is very realistic: it can only deliver a message to one user at
	a time, and it will take some time for it to deliver it, since it needs
	to fly all the way to your message's receiver (how long time it takes
	does of course depends on how long it needs to fly).
</p>